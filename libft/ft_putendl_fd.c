#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	l;

	if (!s || !fd)
		return ;
	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	l = '\n';
	write(fd, &l, 1);
}
